// --------------------------------------------------------------------------
Display::Display(PinName mosi, PinName miso, PinName sclk,
                 PinName cs,   PinName dc,   PinName rst, int freqHz)
    : m_spi(mosi, miso, sclk)
    , m_cs(cs,  1)
    , m_dc(dc,  0)
    , m_rst(rst,1)
{
    m_spi.format(8, 0);
    m_spi.frequency(freqHz);
}

void Display::init()
{
    // Hardware-Reset
    m_rst = 0; thread_sleep_for(50);
    m_rst = 1; thread_sleep_for(120);

    // Software-Reset
    cmd(0x01); thread_sleep_for(150);
    // Sleep out
    cmd(0x11); thread_sleep_for(250);
    // Pixel-Format: 16 Bit
    cmd(0x3A); data8(0x05);
    // MADCTL
    cmd(0x36); data8(0x00);
    // Display an
    cmd(0x29);

    fillScreen(BLACK);
}

// --------------------------------------------------------------------------
void Display::cmd(uint8_t c)
{
    m_dc = 0; m_cs = 0;
    m_spi.write(c);
    m_cs = 1;
}

void Display::data8(uint8_t d)
{
    m_dc = 1; m_cs = 0;
    m_spi.write(d);
    m_cs = 1;
}

void Display::data16(uint16_t d)
{
    m_dc = 1; m_cs = 0;
    m_spi.write(d >> 8);
    m_spi.write(d & 0xFF);
    m_cs = 1;
}

void Display::setWindow(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1)
{
    cmd(0x2A); data8(0); data8(x0); data8(0); data8(x1);
    cmd(0x2B); data8(0); data8(y0); data8(0); data8(y1);
    cmd(0x2C);
}

void Display::fillRect(int x, int y, int w, int h, uint16_t color)
{
    if (w <= 0 || h <= 0) return;
    setWindow(
        static_cast<uint8_t>(x),
        static_cast<uint8_t>(y),
        static_cast<uint8_t>(x + w - 1),
        static_cast<uint8_t>(y + h - 1)
    );
    const uint8_t hi = color >> 8;
    const uint8_t lo = color & 0xFF;
    m_dc = 1; m_cs = 0;
    for (int i = 0; i < w * h; i++) {
        m_spi.write(hi);
        m_spi.write(lo);
    }
    m_cs = 1;
}

void Display::fillScreen(uint16_t color)
{
    fillRect(0, 0, WIDTH, HEIGHT, color);
}

void Display::drawChar(int x, int y, char c, uint16_t fg, uint16_t bg)
{
    if (c < 32 || c > 'z') c = ' ';
    const uint8_t* glyph = s_font[static_cast<uint8_t>(c) - 32];
    for (int col = 0; col < 5; col++) {
        for (int row = 0; row < 7; row++) {
            bool on = (glyph[col] >> row) & 1;
            fillRect(x + col, y + row, 1, 1, on ? fg : bg);
        }
    }
}

void Display::drawText(int x, int y, const char* s, uint16_t fg, uint16_t bg)
{
    int cx = x;
    while (*s) {
        drawChar(cx, y, *s++, fg, bg);
        cx += 6;
        if (cx > WIDTH - 6) { cx = 0; y += 9; }
    }
}
